import { existsSync, mkdirSync, readdirSync, readFileSync, statSync, writeFileSync } from 'node:fs';
import { dirname, extname, join, relative, resolve } from 'node:path';

const root = resolve(new URL('..', import.meta.url).pathname);
const srcRoot = resolve(root, 'src');
const outputPath = resolve(root, 'src/project/notes/generated-draft.js');

const collectFiles = (dir, extensions) => readdirSync(dir)
  .flatMap((entry) => {
    const fullPath = join(dir, entry);
    if (statSync(fullPath).isDirectory()) return collectFiles(fullPath, extensions);
    return extensions.includes(extname(fullPath)) ? [fullPath] : [];
  })
  .sort();

const sourceFiles = collectFiles(srcRoot, ['.js', '.jsx'])
  .filter((file) => !relative(root, file).startsWith('src/project/notes/'));

const existingNotesPath = resolve(root, 'src/project/notes/interactions.js');
const existingNotes = existsSync(existingNotesPath) ? readFileSync(existingNotesPath, 'utf8') : '';
const existingIds = new Set([...existingNotes.matchAll(/id:\s*['"`]([^'"`]+)['"`]/g)].map((match) => match[1]));
const sourceById = new Map();

for (const file of sourceFiles) {
  const text = readFileSync(file, 'utf8');
  const rel = relative(root, file);
  for (const match of text.matchAll(/bind(Interaction|Element)\(\s*['"`]([^'"`]+)['"`]/g)) {
    const kind = match[1] === 'Element' ? 'content' : 'action';
    const id = match[2];
    if (!sourceById.has(id)) {
      sourceById.set(id, {
        id,
        kind,
        file: rel,
        line: text.slice(0, match.index || 0).split('\n').length
      });
    }
  }
}

const allItems = [...sourceById.values()].sort((a, b) => a.id.localeCompare(b.id));
const missingItems = allItems.filter((item) => !existingIds.has(item.id));

const makeTitle = (id) => id
  .split('-')
  .filter(Boolean)
  .map((part) => part.replace(/^\w/, (char) => char.toUpperCase()))
  .join(' ');

const draftItems = missingItems.map((item) => {
  if (item.kind === 'content') {
    return {
      id: item.id,
      kind: 'content',
      type: '待确认内容元素',
      title: `待确认：${makeTitle(item.id)}`,
      purpose: '待确认：说明该内容元素在页面中的用途。',
      dataSource: '待确认：前端静态/前端配置/后端接口/服务端配置/用户生成/缓存或本地状态。',
      bounds: '待确认：字数、比例、截断、空值、加载失败、占位规则。',
      exceptions: '待确认：空状态、加载失败、权限不足、数据异常等处理。',
      reviewed: false
    };
  }
  return {
    id: item.id,
    kind: 'action',
    type: '待确认交互元素',
    title: `待确认：${makeTitle(item.id)}`,
    trigger: '待确认：点击/滑动/长按/输入/上传/系统反馈。',
    purpose: '待确认：说明该交互元素在页面中的用途。',
    effect: '待确认：说明交互后的页面、状态、数据或反馈变化。',
    dataSource: '待确认：前端路由/前端状态/后端接口/服务端配置。',
    bounds: '待确认：输入、上传、频控、权限、状态限制等边界。',
    exceptions: '待确认：失败、取消、空状态、权限不足、接口异常等处理。',
    reviewed: false
  };
});

const fileContent = `// Generated by npm run mlp:generate-docs.
// This is a draft only. Do not treat it as final Product Notes.
// Copy reviewed items into src/project/notes/interactions.js and set reviewed: true only after editor confirmation.

export const generatedDocInventory = ${JSON.stringify(allItems, null, 2)};

export const generatedDocDraft = ${JSON.stringify(draftItems, null, 2)};
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, fileContent);

console.log(`Generated documentation draft: ${relative(root, outputPath)}`);
console.log(`Prototype elements found: ${allItems.length}`);
console.log(`Missing formal notes: ${missingItems.length}`);
if (missingItems.length) {
  console.log('Draft items are reviewed: false and must be editor-confirmed before delivery.');
}
