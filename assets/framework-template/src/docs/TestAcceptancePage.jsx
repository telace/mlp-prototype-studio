function AcceptanceList({ items }) {
  return (
    <ol className="acceptance-list">
      {items.map((item) => <li key={item}>{item}</li>)}
    </ol>
  );
}

export default function TestAcceptancePage() {
  const itemCount = testAcceptanceSections.reduce((total, section) => (
    total
    + (section.items?.length || 0)
    + (section.groups?.reduce((groupTotal, group) => groupTotal + group.items.length, 0) || 0)
  ), 0);

  return (
    <section className="checklist-stage acceptance-stage" aria-label="测试验收必测点">
      <div className="checklist-head">
        <span className="eyebrow">QA acceptance</span>
        <h2>测试验收必测点</h2>
        <p>上线或交付前用于研发、测试、产品共同复核的高频必测清单。当前共 {itemCount} 个检查点。</p>
      </div>
      <div className="acceptance-section-list">
        {testAcceptanceSections.map((section) => (
          <section className="acceptance-section" key={section.title}>
            <h3>{section.title}</h3>
            {section.items ? <AcceptanceList items={section.items} /> : null}
            {section.groups?.map((group) => (
              <article className="acceptance-group" key={group.title}>
                <h4>{group.title}</h4>
                <AcceptanceList items={group.items} />
              </article>
            ))}
          </section>
        ))}
      </div>
    </section>
  );
}
